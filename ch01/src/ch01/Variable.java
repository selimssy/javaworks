package ch01;

public class Variable {

	public static void main(String[] args) {
		
		String name;     // 문자열 변수 name 선언
		name = "한지수";
		
		int grade;    // 정수형 변수 grade 선언
		grade = 2;
		
		int schoolClass = 3;    // 이렇게 한 줄로 선언해도 된다!
		
		
		
		System.out.println(name + "는 " + grade + "학년 " 
				+ schoolClass + "반입니다.");
	
	}

}
